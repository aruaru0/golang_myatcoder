// DSU :
type DSU struct {
	parentOrSize []int
	n            int
}

// newDsu :
func newDsu(n int) *DSU {
	var d DSU
	d.n = n
	d.parentOrSize = make([]int, n)
	for i := 0; i < n; i++ {
		d.parentOrSize[i] = -1
	}
	return &d
}

// Merge :
func (d DSU) Merge(a, b int) int {
	x, y := d.Leader(a), d.Leader(b)
	if x == y {
		return x
	}
	if -d.parentOrSize[x] < -d.parentOrSize[y] {
		x, y = y, x
	}
	d.parentOrSize[x] += d.parentOrSize[y]
	d.parentOrSize[y] = x
	return x
}

// Same :
func (d DSU) Same(a, b int) bool {
	return d.Leader(a) == d.Leader(b)
}

// Leader :
func (d DSU) Leader(a int) int {
	if d.parentOrSize[a] < 0 {
		return a
	}
	d.parentOrSize[a] = d.Leader(d.parentOrSize[a])
	return d.parentOrSize[a]
}

// Size :
func (d DSU) Size(a int) int {
	return -d.parentOrSize[d.Leader(a)]
}

// Groups : original implement
func (d DSU) Groups() [][]int {
	m := make(map[int][]int)
	for i := 0; i < d.n; i++ {
		x := d.Leader(i)
		if x < 0 {
			m[i] = append(m[i], i)
		} else {
			m[x] = append(m[x], i)
		}
	}
	ret := make([][]int, len(m))
	idx := 0
	for _, e := range m {
		ret[idx] = make([]int, len(e))
		copy(ret[idx], e)
		idx++
	}
	return ret
}

type pair struct {
	a, b int
}

func main() {
	defer wr.Flush()
	sc.Split(bufio.ScanWords)
	sc.Buffer([]byte{}, math.MaxInt32)
	// this template is new version.
	// use getI(), getS(), getInts(), getF()
	N, M := getI(), getI()
	a := getInts(N)

	// unionFindで同値で繋がっているパスを結合
	uf := newDsu(N)
	edge := make([]pair, 0)
	for i := 0; i < M; i++ {
		u, v := getI()-1, getI()-1
		if a[u] == a[v] {
			uf.Merge(u, v)
		}
		edge = append(edge, pair{u, v})
	}

	// 有向グラフを作成
	to := make([][]int, N)
	for _, e := range edge {
		u, v := e.a, e.b
		u = uf.Leader(u)
		v = uf.Leader(v)
		if u == v {
			continue
		}
		if a[u] > a[v] {
			u, v = v, u
		}
		to[u] = append(to[u], v)
	}

	// まとめた頂点を除く頂点リストを作成
	vs := make([]int, 0)
	for i := 0; i < N; i++ {
		if uf.Leader(i) == i {
			vs = append(vs, i)
		}
	}

	// 書かれている番号を昇順にソート
	sort.Slice(vs, func(i, j int) bool {
		return a[vs[i]] < a[vs[j]]
	})

	const inf = int(1e18)
	dp := make([]int, N)
	for i := 0; i < N; i++ {
		dp[i] = -inf
	}
	// 番号順に並んでいるので、順番に処理していけば良い
	dp[uf.Leader(0)] = 1
	for _, v := range vs {
		if dp[v] == -inf {
			continue
		}
		for _, u := range to[v] {
			dp[u] = max(dp[u], dp[v]+1)
		}
	}

	ans := dp[uf.Leader(N-1)]
	if ans < 0 {
		out(0)
	} else {
		out(ans)
	}
}
