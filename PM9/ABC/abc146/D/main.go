var col []int

const inf = int(1e10)

func dfs(cur, prev, c int) {
	// if used[cur] {
	// 	return
	// }
	// used[cur] = true
	cnt := 0
	for _, e := range node[cur] {
		if e.to == prev {
			continue
		}
		if cnt == c {
			cnt++
		}
		col[e.idx] = cnt
		dfs(e.to, cur, cnt)
		cnt++
	}
}

type edge struct {
	to, idx int
}

func main() {
	defer wr.Flush()
	sc.Split(bufio.ScanWords)
	sc.Buffer([]byte{}, math.MaxInt32)
	// this template is new version.
	// use getI(), getS(), getInts(), getF()
	N := getI()
	node = make([][]edge, N)
	for i := 0; i < N-1; i++ {
		a, b := getI()-1, getI()-1
		node[a] = append(node[a], edge{b, i})
		node[b] = append(node[b], edge{a, i})
	}

	col = make([]int, N-1)
	dfs(0, -1, inf)

	m := make(map[int]bool)
	for i := 0; i < N-1; i++ {
		m[col[i]] = true
	}
	out(len(m))
	for i := 0; i < N-1; i++ {
		out(col[i] + 1)
	}
}
