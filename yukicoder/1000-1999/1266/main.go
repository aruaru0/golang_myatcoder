// DSU :
type DSU struct {
	parentOrSize []int
	n            int
}

// Dsu :
func Dsu(n int) *DSU {
	var d DSU
	d.n = n
	d.parentOrSize = make([]int, n)
	for i := 0; i < n; i++ {
		d.parentOrSize[i] = -1
	}
	return &d
}

// Merge :
func (d DSU) Merge(a, b int) int {
	x, y := d.Leader(a), d.Leader(b)
	if x == y {
		return x
	}
	if -d.parentOrSize[x] < -d.parentOrSize[y] {
		x, y = y, x
	}
	d.parentOrSize[x] += d.parentOrSize[y]
	d.parentOrSize[y] = x
	return x
}

// Same :
func (d DSU) Same(a, b int) bool {
	return d.Leader(a) == d.Leader(b)
}

// Leader :
func (d DSU) Leader(a int) int {
	if d.parentOrSize[a] < 0 {
		return a
	}
	d.parentOrSize[a] = d.Leader(d.parentOrSize[a])
	return d.parentOrSize[a]
}

// Size :
func (d DSU) Size(a int) int {
	return -d.parentOrSize[d.Leader(a)]
}

// Groups : original implement
func (d DSU) Groups() [][]int {
	m := make(map[int][]int)
	for i := 0; i < d.n; i++ {
		x := d.Leader(i)
		if x < 0 {
			m[i] = append(m[i], i)
		} else {
			m[x] = append(m[x], i)
		}
	}
	ret := make([][]int, len(m))
	idx := 0
	for _, e := range m {
		ret[idx] = make([]int, len(e))
		copy(ret[idx], e)
		idx++
	}
	return ret
}

func main() {
	defer wr.Flush()
	sc.Split(bufio.ScanWords)
	sc.Buffer([]byte{}, 1000000)
	// this template is new version.
	// use getI(), getS(), getInts(), getF()
	N, M, Q := getI(), getI(), getI()

	uf := Dsu(N * 7)
	s := make([][]byte, N)
	for i := 0; i < N; i++ {
		s[i] = []byte(getS())
	}
	set := func(i int) {
		for j := 1; j < 7; j++ {
			if s[i][j] == '1' && s[i][j-1] == '1' {
				uf.Merge(i*7+j, i*7+j-1)
			}
			if s[i][0] == '1' && s[i][6] == '1' {
				uf.Merge(i*7, i*7+6)
			}
		}
	}
	for i := 0; i < N; i++ {
		set(i)
	}

	node := make([][]int, N)
	for i := 0; i < M; i++ {
		u, v := getI()-1, getI()-1
		node[u] = append(node[u], v)
		node[v] = append(node[v], u)
		for j := 0; j < 7; j++ {
			if s[u][j] == '1' && s[v][j] == '1' {
				uf.Merge(u*7+j, v*7+j)
			}
		}
	}

	for i := 0; i < Q; i++ {
		op, x, y := getI(), getI()-1, getI()-1
		if op == 2 {
			out(uf.Size(x * 7))
		} else {
			s[x][y] = '1'
			set(x)
			for _, e := range node[x] {
				for j := 0; j < 7; j++ {
					if s[x][j] == '1' && s[e][j] == '1' {
						uf.Merge(x*7+j, e*7+j)
					}
				}
			}
		}
	}
}
